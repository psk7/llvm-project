#ifndef LLVM_Z80_INST_PRINTER_H
#define LLVM_Z80_INST_PRINTER_H

#include "llvm/MC/MCInstPrinter.h"

#include "MCTargetDesc/Z80MCTargetDesc.h"

namespace llvm {

/// Prints Z80 instructions to a textual stream.
class Z80InstPrinter : public MCInstPrinter {
public:
  Z80InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                 const MCRegisterInfo &MRI);

  static const char *getPrettyRegisterName(unsigned RegNo,
                                           MCRegisterInfo const &MRI);

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

private:
  static const char *getRegisterName(unsigned RegNo,
                                     unsigned AltIdx = Z80::NoRegAltName);

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printPCRelImm(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printPCRelImm(const MCInst *MI, uint64_t /*Address*/, unsigned OpNo,
                     raw_ostream &O) {
    printPCRelImm(MI, OpNo, O);
  }
  void printMemri(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printAddr8(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printRegref(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printMemriz(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printCondCode(const MCInst *MI, unsigned OpNo, raw_ostream &O);

  // Autogenerated by TableGen.
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &O);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                               unsigned OpIdx, unsigned PrintMethodIdx,
                               raw_ostream &O);

public:
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
};

} // end namespace llvm

#endif // LLVM_Z80_INST_PRINTER_H

